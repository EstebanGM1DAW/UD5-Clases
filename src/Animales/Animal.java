package Animales;

public class Animal {
    String tipo, nombre, nombreCien;
    int edad;
    int obtenerEdad() {
        return edad;
    }
    String obtenerTipo(){
        return tipo;
    }
    String obtenerNombre(){
        return nombre;
    }
    String obtenerNombreCientifico(){
        return nombreCien;
    }
}
